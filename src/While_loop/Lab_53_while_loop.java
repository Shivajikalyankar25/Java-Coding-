package While_loop;

public class Lab_53_while_loop {
	public static void main(String[] args) {
		// its sister of for loop
		// verify the condition before executing the block.
		// when first time condition is false the block will not be executed
		//for and while statement also called as Entry controlled loops.
		//SYntax
		//entry control first condition should be true
		int i = 0;
		while(i<=10) {
			System.out.println(i);
			i++;
		}
		System.out.println("end");
	} 

}
