package Operators;

public class Lab_14_Arithmetic_Operatores {
	public static void main(String[] args) {
		//Arithmetic operators
		int a = 20;
		int b = 25;
		float c = 4.005f;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a+c);	
		System.out.println(a/c);
	}

}
