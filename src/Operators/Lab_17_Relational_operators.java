package Operators;

public class Lab_17_Relational_operators {
	public static void main(String[] args) {
		//it will always is boolean statement
		int d  = 20;
		int g = 30 ;
		System.out.println(d<g);
		System.out.println(d>g);
		System.out.println(d<=g);
		System.out.println(d>=g);
		System.out.println(d=g);
		System.out.println(d!=g);
	}

}
